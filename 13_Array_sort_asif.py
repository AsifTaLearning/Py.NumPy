import numpy as np

# Sorting Arrays

arr = np.array([3, 2, 0, 1])

print(np.sort(arr))

# Another example

arr = np.array(['banana', 'cherry', 'apple'])

print(np.sort(arr))

# Another example

arr = np.array([True, False, True])

print(np.sort(arr))

# Sorting a 2-D Array

arr = np.array([[3, 2, 4], [5, 0, 1]])

print(np.sort(arr))



